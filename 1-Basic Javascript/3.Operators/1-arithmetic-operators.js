/* ARITHMETIC OPERATORS */

let x = 10;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);
console.log(x ** y);  // x to the power of y


// Increment (++)

// console.log(++x);
console.log(x++);
console.log(x);

// Decrement (--)

// console.log(--x);
console.log(x--);
console.log(x);